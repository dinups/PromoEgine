package com.promo.engine.vo;

public class FixedPriceVo {

	private String sku;
	private int itemsNo;
	private int price;

	public String getSku() {
		return sku;
	}

	public void setSku(String sku) {
		this.sku = sku;
	}

	public int getItemsNo() {
		return itemsNo;
	}

	public void setItemsNo(int itemsNo) {
		this.itemsNo = itemsNo;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
